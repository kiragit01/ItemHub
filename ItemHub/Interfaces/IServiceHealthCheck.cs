namespace ItemHub.Interfaces;

/// <summary>
/// Единый интерфейс для проверки работоспособности внешнего сервиса.
/// Метод TryCheck возвращает текущее кэшированное состояние сервиса.
/// При необходимости инициирует асинхронную проверку, чтобы обновить состояние.
/// </summary>
public interface IServiceHealthCheck
{
    /// <summary>
    /// Возвращает текущее состояние сервиса.
    /// Если с момента последней проверки прошло достаточно времени, инициирует асинхронную проверку.
    /// </summary>
    bool TryCheck();
}
